    {
        $name_menu = 'farm';

        $title = $farm->menu($id, $name_menu);
        if(!$title) abort(404);

        $incomes = $farm->farm($id);

        return view('pages.farm', [
            'title' => $title,
            'farm_menu' => menu($name_menu, 'menu.farm-menu'),
            'incomes' => $incomes,
        ]);
    }

